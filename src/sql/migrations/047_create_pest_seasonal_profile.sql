-- Migration 047: Create pest_seasonal_profile reference table
-- Part of the monitoring prioritization feature (see docs/PLAN_PRIORIZACION_MONITOREO.md, stage P0).
--
-- One-time import of the pest-forecast POC's already-verified, harmonized historical
-- monitoring data (analysis/pest-forecast-poc/data/processed/monitoreo_lote.parquet,
-- 2022-07 through 2026-07, lote-level, source-tagged excel_2023/excel_2024/db) into a
-- lightweight seasonal-calendar reference table. This is NOT derived from a live query
-- and NOT re-run automatically -- it is a frozen snapshot seeded once, giving the live
-- app 4 years of seasonal signal instead of the ~1.5 years currently in `monitoreos`.
--
-- ============================================================================
-- POOLING DECISION (grain of this table): farm-wide per pest_id, lote_id = NULL
-- for every seed row. Documented here per the P0 task's explicit request to justify
-- the choice, since the design doc left it open.
--
-- Data-density check performed before deciding (on the cleaned parquet, 9 real lotes,
-- 31 pest_keys, all mapped to real catalog rows):
--   - Per (lote_key, pest_key, ISO week) cell: 4,252 distinct cells after dropping the
--     2 rows for the non-current lote '7. El Triunfo' (see below) -- median 1 row/cell,
--     mean 1.38 rows/cell, mean 1.35 distinct years observed per cell. Far too thin to
--     compute a stable modal tier at lote grain -- most cells have exactly one
--     observation ever, so "the historical tier for this lote/pest/week" would really
--     just be "whatever the single round happened to read," not a seasonal pattern.
--   - Pooling across all 9 lotes for a given pest_key: 875 distinct (pest_key, week)
--     cells, mean 6.7 rows/cell, mean 1.65 distinct years observed per cell. Still not
--     abundant (many cells still rest on 1-3 years), but a meaningful, real improvement
--     over the lote-grain numbers above.
--   - Pest emergence timing (temperature/rainfall-driven lifecycle stages,
--     flowering-linked pests, etc.) is primarily a function of regional climate and
--     pest biology, both effectively uniform across this one farm's lotes -- they sit
--     in the same micro-region (see CLAUDE.md's Ecowitt/OpenWeather farm-coordinates
--     note, a single lat/lon serves the whole farm). There is no strong agronomic
--     reason to expect lote A's thrips season to differ in timing from lote B's.
--     CAVEAT (flagged by independent review, not fully resolved by the density argument
--     alone): the farm's lotes sit on Andean terrain (Aguadas, Caldas) where altitude
--     can vary meaningfully lote-to-lote, and altitude does affect temperature-driven
--     pest phenology in principle. The pooling decision here is ultimately forced by
--     data density regardless (median 1 obs/cell makes per-lote modal tiers meaningless
--     with or without an altitude effect) -- but if per-sublote history accumulates
--     enough in future years to test this, an altitude-stratified profile would be worth
--     revisiting rather than assuming uniformity is fully correct.
--
-- Conclusion: pool across lotes. The `lote_id` column is kept nullable (per the design
-- doc's original column list) rather than dropped, so a future recomputation with more
-- years of sublote-level data (2025+) COULD populate real lote-specific rows without a
-- schema change -- but every row from this one-time import has lote_id = NULL, meaning
-- "farm-wide seasonal profile for this pest." This also matches known limitation #3 in
-- docs/PLAN_PRIORIZACION_MONITOREO.md (2023-2024 history is lote-level only; real
-- sublote-level history only starts in 2025) -- pooling is the necessary generalization
-- to make this multi-year history usable at all, rather than quietly overfitting to
-- single-observation cells.
--
-- `historical_tier` methodology, matching the POC's S4 climatology baseline
-- (src/s4_baselines.py:baseline_climatology / src/s3_panel.py:compute_tier_thresholds),
-- adapted for a one-time reference build with no train/test split (this whole table IS
-- historical reference data -- there is no "future" to leak from):
--   1. Per pest_id, tertile-threshold incidencia_pct across the ENTIRE cleaned parquet
--      history (all lotes, all years) -- low_max = 1/3 quantile, med_max = 2/3 quantile.
--   2. Every row gets Low/Med/High via those pest-specific thresholds.
--   3. Group by (pest_id, ISO week_of_year), pooled across lotes and years: the modal
--      (most frequent) tier is `historical_tier`; `n_years_observed` is the count of
--      distinct calendar years contributing to that cell.
-- ============================================================================
--
-- Source-to-catalog mapping: `pest_key`/`lote_key` in the parquet are already
-- normalized by the POC's normalize.py to match live `plagas_enfermedades_catalogo
-- .nombre` / `lotes.nombre` exactly (case-fixed, whitespace-trimmed). All 31 distinct
-- pest_keys resolved to real catalog rows on exact/trimmed string match -- no
-- unresolved pests. One lote_key, '7. El Triunfo' (2 rows), does not correspond to any
-- current real lote and was dropped (logged, not silently discarded) -- see report.
--
-- 875 seed rows total (31 pests x up to 52 ISO weeks, sparse where a pest was never
-- observed in a given week across 4 years of history).

CREATE TABLE IF NOT EXISTS pest_seasonal_profile (
  id                UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  pest_id           UUID NOT NULL REFERENCES plagas_enfermedades_catalogo(id),
  lote_id           UUID NULL REFERENCES lotes(id),
  week_of_year      SMALLINT NOT NULL CHECK (week_of_year BETWEEN 1 AND 53),
  historical_tier   TEXT NOT NULL CHECK (historical_tier IN ('Low', 'Med', 'High')),
  n_years_observed  SMALLINT NOT NULL CHECK (n_years_observed > 0),
  source            TEXT NOT NULL DEFAULT 'poc_2023_2026_import',
  created_at        TIMESTAMPTZ NOT NULL DEFAULT now()
);

-- One profile row per (pest, lote-or-farm-wide, week). Two partial unique indexes
-- (rather than one index with a NULL sentinel) because every row from this import has
-- lote_id IS NULL (farm-wide pooling decision above); this still holds correctly if a
-- future recomputation adds real per-lote rows alongside the farm-wide ones.
CREATE UNIQUE INDEX idx_pest_seasonal_profile_unique_farmwide
  ON pest_seasonal_profile (pest_id, week_of_year) WHERE lote_id IS NULL;
CREATE UNIQUE INDEX idx_pest_seasonal_profile_unique_perlote
  ON pest_seasonal_profile (pest_id, lote_id, week_of_year) WHERE lote_id IS NOT NULL;

CREATE INDEX idx_pest_seasonal_profile_pest_id ON pest_seasonal_profile(pest_id);
CREATE INDEX idx_pest_seasonal_profile_lote_id ON pest_seasonal_profile(lote_id);
CREATE INDEX idx_pest_seasonal_profile_week ON pest_seasonal_profile(week_of_year);

-- RLS: authenticated users can read; writes are hand-managed via migration (no
-- INSERT/UPDATE/DELETE policy), matching the pattern used for pest_umbral_economico
-- (migration 048) and other reference tables (e.g. clima_resumen_diario, migration 035).
ALTER TABLE pest_seasonal_profile ENABLE ROW LEVEL SECURITY;

CREATE POLICY "pest_seasonal_profile_select_authenticated"
  ON pest_seasonal_profile FOR SELECT TO authenticated USING (true);

